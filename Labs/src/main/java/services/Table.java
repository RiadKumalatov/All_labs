package services;

public class Table {

    private int tableNumber;
    private int tableFloor;
    private int tablePlaces;

    public int getTableNumber() {
        return tableNumber;
    }

    public void setTableNumber(int tableNumber) {
        this.tableNumber = tableNumber;
    }

    public int getTableFloor() {
        return tableFloor;
    }

    public void setTableFloor(int tableFloor) {
        this.tableFloor = tableFloor;
    }

    public int getTablePlaces() {
        return tablePlaces;
    }

    public void setTablePlaces(int tablePlaces) {
        this.tablePlaces = tablePlaces;
    }
}
